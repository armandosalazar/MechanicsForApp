package me.armandosalazar.mechanicsforapp.recyclerview;

public class Item {

    private String name;
    private String typeOfMechanic;
    private int image;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTypeOfMechanic() {
        return typeOfMechanic;
    }

    public void setTypeOfMechanic(String typeOfMechanic) {
        this.typeOfMechanic = typeOfMechanic;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }
}
